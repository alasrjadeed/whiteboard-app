// Helper function to get translation
export const t = (key: string, language: string = 'en'): string => {
  return (translations[language as keyof typeof translations]?.[key as keyof typeof translations] as string) || 
         translations.en[key as keyof typeof translations] || 
         key;
};

// Get current language direction
export const getDirection = (language: string): 'ltr' | 'rtl' => {
  return languages[language as keyof typeof languages]?.direction || 'ltr';
};
